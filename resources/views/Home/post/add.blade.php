					if ((i + 2) == facePage.length) { $(obj).addClass('page-next-n').removeClass('page-next'); }
					i = facePage.length;
				}
			}
		}
		if (goWhere == 'prev') {  //上一页
			for (i = (facePage.length - 1); i > 0; i--) {
				if (!facePage.eq(i).is(':hidden') && facePage.first().is(':hidden')) {
					var faceobj = facePage[i].id;
