					facePage.eq(i).hide().prev().show();
					$(obj).prev().addClass('page-next').removeClass('page-next-n');
					if ((i - 1) == 0) { $(obj).addClass('page-prev-n').removeClass('page-prev'); }
					i = 0;
				}
			}
		}
	}


